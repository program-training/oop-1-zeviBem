//  1. Rectangle - מלבן
class Rectangle {
    width: number;
    hight: number;

    constructor(width: number, hight: number) {
        this.width = width;
        this.hight = hight;
    }
    area(): number {
        return (this.width * this.hight) / 2
    }
    
}

